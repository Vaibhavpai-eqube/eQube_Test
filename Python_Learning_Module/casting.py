# Casting in python is therefore done using constructor functions:
print(int(1.1))  # 1
print(float(1))  # 1.0
print(str(1))    # '1'
# Casting is the process of converting a value from one type to another.
# It is also known as type conversion.
# In Python, casting is done using built-in functions.
# The most common types are int, float, and str.
# Casting is useful when you want to change the type of a value to perform certain operations.
# For example, if you want to perform arithmetic operations on a string that represents a number,
# you need to convert it to an integer or float first.